import matplotlib.pyplot as plt
from matplotlib.ticker import FormatStrFormatter
import matplotlib.colors as mcolors

fig, axes = plt.subplots(3, 3, figsize=(12,10), constrained_layout=True)
axes = axes.flatten()

vmin, vmax = -1, 1  # NDVI range

# Use x and y as lon/lat
lon = da.x.values
lat = da.y.values

# NDVI colormap: brown → yellow → green
ndvi_cmap = plt.get_cmap('RdYlGn')
norm = mcolors.Normalize(vmin=vmin, vmax=vmax)

for i, year in enumerate(years):
    ndvi = da.sel(year=year)
    img = ndvi.squeeze().values

    im = axes[i].imshow(
        img, cmap=ndvi_cmap, norm=norm,
        extent=[lon.min(), lon.max(), lat.min(), lat.max()],
        origin='upper'
    )

    axes[i].set_title(f"NDVI {year}", fontsize=14)
    axes[i].set_xlabel('Longitude', fontsize=12)
    axes[i].set_ylabel('Latitude', fontsize=12)

    # Format tick labels to one decimal place
    axes[i].xaxis.set_major_formatter(FormatStrFormatter('%.1f'))
    axes[i].yaxis.set_major_formatter(FormatStrFormatter('%.1f'))

# Add colorbar
cbar = fig.colorbar(im, ax=axes, orientation='vertical', fraction=0.03, pad=0.02)
cbar.set_label('NDVI', fontsize=14)
cbar.set_ticks([-1, -0.5, 0, 0.25, 0.5, 0.75, 1])
cbar.ax.set_yticklabels(['<0', '-0.5', '0', '0.25', '0.5', '0.75', '1'])

plt.show()

