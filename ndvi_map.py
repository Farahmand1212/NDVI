s2 = (ee.ImageCollection("COPERNICUS/S2_SR")
      .filterBounds(aoi)
      .filter(ee.Filter.lt('CLOUDY_PIXEL_PERCENTAGE',20)))

def add_ndvi(img):
    return img.addBands(img.normalizedDifference(['B8','B4']).rename('NDVI'))

s2 = s2.map(add_ndvi)

years = list(range(2018, 2027))
ndvi_rasters = []

for year in years:
    start = ee.Date.fromYMD(year,1,1)
    end   = ee.Date.fromYMD(year,12,31)
    yearly = s2.filterDate(start,end)
    mean_ndvi = yearly.select('NDVI').mean().clip(aoi)

    # Export to local temp GeoTIFF
    filename = f'NDVI_{year}.tif'
    geemap.ee_export_image(mean_ndvi,
                           filename=filename,
                           scale=10,
                           region=aoi)

    ndvi_rasters.append(filename)

ndvi_arrays = []

for f in ndvi_rasters:
    ds = rioxarray.open_rasterio(f, masked=True)
    ndvi_arrays.append(ds)

# Stack into xarray DataArray with 'year' dimension
da = xr.concat(ndvi_arrays, dim=pd.Index(years, name='year'))

import matplotlib.pyplot as plt

